    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        PreparedStatement ps = null;
        ResultSet rs= null;
        try
        {
            ClsConexion con= new ClsConexion();
            Connection conn = con.getConnection();
            
            int fila = jTable1.getSelectedRow();
            String codigo = jTable1.getValueAt(fila, 0).toString();
            
            ps=conn.prepareStatement("select * from tb_comisiones where codigo=?");
            ps.setString(1, codigo);
            rs=ps.executeQuery();
            
            while(rs.next()){
                Text1.setText(rs.getString("codigo"));
                Text2.setText(rs.getString("nombre"));
                Text3.setText(rs.getString("enero"));
                Text4.setText(rs.getString("febrero"));
                Text5.setText(rs.getString("marzo"));
            }
            
        }catch(SQLException ex){
            System.out.println(ex.toString());
        }
            
    }//GEN-LAST:event_jTable1MouseClicked

