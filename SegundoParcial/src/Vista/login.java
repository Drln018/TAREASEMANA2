    public void validarUsuario(){  
        PreparedStatement ps= null;
        ResultSet rs= null;
        int result=0;
        //convertir a texto
        String pass= String.valueOf(TextPassword.getPassword());
        String usuario=TUsuario.getText();
        
        try
        {
            ClsConexion con = new ClsConexion();
            Connection conn = con.getConnection();
            String sql="select * from login where usuario='"+ usuario+"' and contrase√±a='"+pass+"'";
            ps=conn.prepareStatement(sql);
            rs= ps.executeQuery();
    
        // ver si se encuentra el ususario
        if(rs.next()){//recorrer todos los registros
            result=1;
            if(result==1){
                //abrir el sistema
                Vendedores form=new Vendedores();
                form.setVisible(true);
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Error de Acceso, el usuario no esta registrado");
        }
      
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error "+e.getMessage());
            System.out.println(e);
        }   
    }
    
